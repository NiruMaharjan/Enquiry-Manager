    private void clearFields() {
        txtname.setText(" ");
        txtdescription.setText(" ");
        txtfees.setText("");
        chkactive.setSelected(false);

    }

    private boolean isNumeric(String txt) {
        try {
            Integer.parseInt(txt);
            return true;
        } catch (NumberFormatException ex) {
            return false;
        }
    }
