    private void saveButtonClick(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonClick
        Course c = new Course();
        c.setName(txtname.getText());
        c.setDescription(txtdescription.getText());
        int fees = 0;
        if (!txtfees.getText().equalsIgnoreCase("") && isNumeric(txtfees.getText())) {
            fees = Integer.parseInt(txtfees.getText());
        }
        c.setFees(fees);
        c.setStatus(chkactive.isSelected());
        try {
            coursedao.insert(c);
            btnSave.setEnabled(false);
            btnadd.setEnabled(true);
            clearFields();

        } catch (ClassNotFoundException | SQLException ce) {
            JOptionPane.showMessageDialog(null, ce.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_saveButtonClick

